function cityCountry(city:string, country:string) :string{
    return`${city},${country}`
}

let c1=cityCountry(`Karachi`,`Pakistan`);
let c2=cityCountry(`Lahore`,`Pakistan`);
let c3=cityCountry(`Istanbul`,`Turkey`);
let c4=cityCountry(`Paris`,`France`);
console.log(c1);
console.log(c2);
console.log(c3);
console.log(c4);


