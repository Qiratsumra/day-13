function cityCountry(city, country) {
    return "".concat(city, ",").concat(country);
}
var c1 = cityCountry("Karachi", "Pakistan");
var c2 = cityCountry("Lahore", "Pakistan");
var c3 = cityCountry("Istanbul", "Turkey");
var c4 = cityCountry("Paris", "France");
console.log(c1);
console.log(c2);
console.log(c3);
console.log(c4);
